    <p>Recent advances in determining the origin of western civilization and the settlement of Europe are 
based especially on findings in genetics, archeology and linguistics. The papers on the topic given at 
a conference that brought together eminent specialists in these fields under organization of the Banco 
Popolare di Milano have been published in Italian under the title <i lang='it'>Le radici prime dell'Europa. 
Gli intrecci genetici, linguistici, storici</i>, edited by Gianluca Bocchi and Mauro Ceruti (Milan: Bruno 
Mondadori, 2001). While these three sciences all provide information on the settlement, only through 
linguistics can the people involved be identified. Yet linguistics dealing with the early period is least 
advanced of the three. Moreover, grammars published as introductions to the early languages are produced 
on the pattern of those designed for instruction of secondary school students of years past, who were 
expected to take eight years of Latin, six of Greek, and then proceed to the study of Sanskrit and other 
less widely studied languages like Old Slavic, Armenian, and Avestan. Under curricula of today, few 
scholars find such a course of study acceptable.</p>

<p>Moreover, the important ability with respect to these languages is that of reading texts, with or 
without the help of translations. The online introductions in <i>Early Indo-European Online</i>
are designed to provide such ability. In this series, texts that in themselves are valuable for 
literary and historical as well as linguistic purposes are briefly introduced, glossed word-by-word, 
followed by grammatical descriptions, and accompanied by a complete glossary, a base-form dictionary,
and an English meaning index. For example, the third through fifth units of the introduction to Latin 
contain Julius Caesar's descriptions of the early Germanic people, which we assume from our reading of 
Herodotus and other early historians might also apply to the Indo-European peoples several millennia 
earlier. Other texts are important selections of literature, such as the opening lines of the <i>Iliad</i> 
and the <i>Odyssey</i>.  Still others are important theological texts.</p>

<p>Most of this work has been carried out under grants from the <i>Salus Mundi</i> Foundation, for which we owe 
thanks to Dr. A. Richard Diebold, Jr.  We hope that these lessons have wide usage, but would also appreciate 
users giving credit to him and to <i>Salus Mundi</i> for the grant that made possible the introductions 
to Latin and Greek and, through follow-on grants, subsequent works, all part of the Linguistics Research 
Center collection.  We welcome comments, which may be sent to the Linguistics Research Center via the
e-mail "comments" link at the bottom of each page.</p>

