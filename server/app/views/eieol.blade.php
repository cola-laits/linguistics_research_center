<h4>About the Series Lessons</h4>

<p>The texts in these lessons were selected for the historical and cultural information they provide;
they have not been simplified, but sections may be omitted.  Each lesson series comprises a number of 
glossed texts (usually ten), each with a brief introduction identifying its author and the document 
from which it was taken, an English translation, and five Grammar points; other resources in each 
series include a Table of Contents and a Series Introduction, a Master Glossary of words covering all 
lesson texts, a Base Form Dictionary also spanning the texts, and an English Meaning Index to the 
glosses.  Listed in order of [first] online publication:</p>

<p><b>Latin Online</b> [2002] is designed to teach you to read Latin, or to improve your reading knowledge.  
After completing the course, you should be able to read any Latin texts. You may find it easier to use texts 
with translations, such as the Loeb Classics.</p>

<p><b>Classical Greek Online</b> [2002], likewise, is designed to teach you to read classical Greek texts or 
to improve your reading knowledge.  <b>New Testament Greek Online</b> [2003] includes some of the central N.T. 
passages; it is designed like Greek Online.</p>

<p><b>Old Church Slavonic Online</b> [2003] is written in the same format with the same goals in mind.  Seven 
OCS texts are taken from the New Testament; two of these (lessons 6, 7) parallel texts in our New Testament 
Greek series.  Three non-Biblical texts, from other sources, are included for literary variety. An annotated 
bibliography, rather than being included as lesson points 46-50 in lesson 10 as in the Latin/Greek series, 
is listed separately; see the link at the bottom of the OCS Series Introduction page.</p>

<p><b>Classical Armenian Online</b> [2004] is a collection of 5 lessons, with texts dated from the 5th to 7th 
centuries A.D.  Again, the annotated bibliography is separate; see the link at the bottom of the Armenian 
Series Introduction page.</p>

<p><b>Old Iranian Online</b> [2004] is a 10-lesson series in which two related languages are covered: Avestan (lessons 
1-6), with texts from the 10th - 6th centuries B.C., and Old Persian (7-10), with texts from the 6th - 5th centuries 
B.C.  Each language has its own brief annotated bibliography.  For Avestan, two dialects are covered: "old" (lessons 1-4)
and "young[er]" (5-6).  A short list of works for Further Reading also appears at the end of the Series Introduction.</p>

<p><b>Old Norse Online</b> [2005] is a 10-lesson series, with texts from the 9th - 14th centuries A.D.  A separate
annotated bibliography is included (see link at bottom of Series Introduction page).</p>

<p><b>Baltic Online</b> is a collection of 7 Lithuanian lessons [2005] covering texts from the 16th - 20th centuries A.D.,
plus an additional set of 3 Latvian lessons [2007] covering texts from the 16th - 19th centuries A.D.</p>

<p><b>Hittite Online</b> [2005] is a 10-lesson series with texts from the 17th - 12th centuries B.C.  A short 
bibliography appears at the end of the Series Introduction.</p>

<p><b>Ancient Sanskrit Online</b> [2006] is a 10-lesson series with texts from the <i>Rigveda</i>, dating perhaps from 
the beginning of the 2nd millennium B.C.  A separate tabular index to Rigvedic passages covered in the series is included 
(see link at bottom of Series Introduction page). After this series was completed, an online version of the full
<a title="The Rigveda: Metrically Restored Text" href="http://www.utexas.edu/cola/centers/lrc/RV/">metrically restored Rigveda text</a>
was prepared; it is transcribed in Unicode.</p>

<p><b>Gothic Online</b> [2006] is a 10-lesson series with texts from the Gothic <i>New Testament</i> and from 
<i>Skeireins</i>, together dated in the 4th - 5th centuries A.D.  Half of these texts (lessons 1, 4, 6, 7, 10) parallel 
texts in our New Testament Greek series.</p>

<p><b>Old French Online</b> [2006] is a 10-lesson series with texts from the 9th - 13th centuries A.D.  Grammar
points 46-50, in lesson 10, comprise a short bibliography.</p>

<p><b>Old Irish Online</b> [2006-07] is a 10-lesson series with texts from the 6th - 10th centuries A.D.</p>

<p><b>Old English Online</b> [2007] is a 10-lesson series with texts from the 7th - 10th centuries A.D.</p>

<p><b>Tocharian Online</b> [2007-10] is a 10-lesson series with a separate annotated bibliography; Tocharian texts 
are dated in the 6th - 8th centuries A.D. We believe this to be the first introductory Tocharian grammar published 
in English.</p>

<p><b>Albanian Online</b> [2011] is a collection of 3 Standard Albanian lessons covering texts from the 20th - 21st 
centuries A.D., plus an additional pair of Geg lessons covering texts from the 16th &amp; 19th centuries A.D.</p>

<p><b>Old Russian Online</b> [2013] is a 10-lesson series with texts from the 10th - 15th centuries A.D.</p>



